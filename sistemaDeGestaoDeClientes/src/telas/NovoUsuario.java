    private void btnNovoUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoUsuarioActionPerformed
        String usu, senha;
        usu = txtUsu.getText();
        senha = pswUsu.getText();

        try {
            new SistemaGestaoDao().cadastrarUsuario(usu, senha);

            JOptionPane.showMessageDialog(null, "Usuario  Cadastrado com Sucesso");

            txtUsu.setText("");
            pswUsu.setText("");

        }catch (ClassNotFoundException | SQLException ex){
            if(ex.getMessage().contains("Duplicate entry")){
                JOptionPane.showMessageDialog(null, "Usuario j√° cadastrado");
            } else {
                JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnNovoUsuarioActionPerformed

    private void ckMostrarSenhaUsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ckMostrarSenhaUsuActionPerformed

        if(ckMostrarSenhaUsu.isSelected()){
            pswUsu.setEchoChar((char)0);
        }else {
            pswUsu.setEchoChar('*');
        }
    }//GEN-LAST:event_ckMostrarSenhaUsuActionPerformed

