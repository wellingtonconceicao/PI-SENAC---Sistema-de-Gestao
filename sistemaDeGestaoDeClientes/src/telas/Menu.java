    private void btnBuscarProdNovaCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProdNovaCompraActionPerformed
        String nomeProd;
        nomeProd = txtNomeProdNovaCompra.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("jdbc:mysql://bdremotosenac.mysql.database.azure.com:3306/remotosenac", "rootwellington", "Well1997##");

            PreparedStatement st = conectar.prepareStatement("SELECT * FROM produtos WHERE nomeProd LIKE ?");
            st.setString(1, "%" + nomeProd + "%");

            ResultSet produto = st.executeQuery();
            DefaultTableModel tblModelo = (DefaultTableModel) tblNovaCompraProd.getModel();
            tblModelo.setRowCount(0);

            while (produto.next()) {
                String linha[] = {
                    produto.getString("cod_prod"),
                    produto.getString("nomeProd"),
                    produto.getString("qtd"),};
                tblModelo.addRow(linha);

            }

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnBuscarProdNovaCompraActionPerformed

