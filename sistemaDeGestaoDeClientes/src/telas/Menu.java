    private void btnRealizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarCompraActionPerformed
        String cpfCli = fmtCPFCliNovaCompra.getText();
        String nomeProd = txtNomeProdNovaCompra.getText();
        String nomeCli = txtNomeCliPedido.getText();

        if (cpfCli.isEmpty() || nomeProd.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campos Vazios! Selecione Um Cliente e Um Produto! ");
        } else {

            try {
                int quantidade = Integer.parseInt(txtQtdProdutoPedido.getText());
                int codProd = Integer.parseInt(txtCodProdPedido.getText());

                new SistemaGestaoDao().novoPedido(cpfCli, nomeCli, codProd, nomeProd, quantidade);
                JOptionPane.showMessageDialog(null, "Compra Realizada com Sucesso");

                fmtCPFCliNovaCompra.setText("");
                txtNomeProdNovaCompra.setText("");

                txtCPFCliPedidoNovaCompra.setText("");
                txtNomeCliPedido.setText("");
                txtCodProdPedido.setText("");
                txtNomeProdPedido.setText("");
                txtQtdProdutoPedido.setText("");

            } catch (ClassNotFoundException | SQLException ex) {

                JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "[ERRO] Quantidade Vazia! Insira '' 0 '' se não houver unidades do Produto!");

            }

        }
    }//GEN-LAST:event_btnRealizarCompraActionPerformed

