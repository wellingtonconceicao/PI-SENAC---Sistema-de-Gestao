    private void btnCadastrarCliente1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarCliente1ActionPerformed
        String nomeCli, sobrenomeCli, cpfCli, dataNascCli, sexoCli, generoCli, nomeSocialCli, telFixoCli, celularCli, emailCli;

        nomeCli = txtNome.getText();
        sobrenomeCli = txtSobrenome.getText();
        dataNascCli = fmtData.getText();
        cpfCli = fmtCPF.getText();
        sexoCli = cmbSexoBiologico.getSelectedItem().toString();
        generoCli = cmbIdentidadeDeGenero.getSelectedItem().toString();
        nomeSocialCli = txtNomeSocial.getText();
        telFixoCli = txtFixo.getText();
        celularCli = txtCelular.getText();
        emailCli = txtEmail.getText();
        if (nomeCli.isEmpty() || sobrenomeCli.isEmpty() || dataNascCli.isEmpty() || cpfCli.isEmpty()) {
            JOptionPane.showMessageDialog(null, "[ERRO] Existem Campos Vazios!");
        } else
         try {
            new SistemaGestaoDao().cadastrarCliente(nomeCli, sobrenomeCli, dataNascCli, cpfCli, sexoCli, generoCli, nomeSocialCli, telFixoCli, celularCli, emailCli);
            JOptionPane.showMessageDialog(null, "Cliente Cadastrado com Sucesso");
            txtNome.setText("");
            txtSobrenome.setText("");
            fmtCPF.setText("");
            fmtData.setText("");
            txtNomeSocial.setText("");
            txtFixo.setText("");
            txtEmail.setText("");
            txtCelular.setText("");
            fmtData.setText("");
            fmtCPF.setText("");

            listarClientes();

        } catch (ClassNotFoundException | SQLException ex) {
            if (ex.getMessage().contains("Duplicate entry")) {
                JOptionPane.showMessageDialog(null, "Usuário já cadastrado");
            }
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());

        }
    }//GEN-LAST:event_btnCadastrarCliente1ActionPerformed

